package day1basics;

public class PrintNum {
    public static void main(String[]args)
    {
        //for loop
        for(int i=1;i<=33;i++)
        {
            System.out.println(i);
        }
        //while loop
        int j=34;
        while(j<=66)
        {
            System.out.println(j);
            j++;
        }
        //do while loop
        int k=67;
        do
        {
            System.out.println(k);
            k++;
        }while(k<=100);
    }
}
